package com.day13.SDLC;

public class Agile_Model {

	//AGILE MODEL - 
	
	/*
	 	Instead of building the whole product at once, 
	 	teams work in short, rapid iterations (typically 1-4 weeks) 
	 	to develop and deliver functional parts of the software, 
	 	allowing for frequent feedback, continuous improvement, 
	 	and quick adaptation to changes.
	 	
	 	- Repeated , iterative model
	 	- incremental approach - small manageable chunks
	 	- most popular and practical sdlc
	 	- sprints - 2 to 4 weeks
	 	- delivered - working version of the product
	 	- tested and improved in the next sprint
	 	- 
	 	-
	 	- Example :
	 	- sprint 1 = build design and key modules
	 				- 2 weeks  - sample avg model done
	 				- deploy and get client feedback
	 	- sprint 2 = add new features
	 			   - integrate to APIs like MYSQL , React.js, MAPs api
	 			   - client test  -> feedback -> maintain flow
	 	- sprint (n) - ...........
	 	
	 			   
	 			    			   
	 	TOOLS - 
	 		JIRA = sprint planning / tracking
	 		TRELLO - can also be used
	 		CONFLUENCE - centralized workspace for documentation
	 		GITHUB /GIT LAB /GIT BUCKET  - version control
	 	
	 	
	  
	 */
}
